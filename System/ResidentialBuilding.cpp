#ifndef RESIDENTIALBUILDING_H
#define RESIDENTIALBUILDING_H

#include "Building.h"

/**
 * @brief A class representing residential buildings in the city
 * @details ResidentialBuilding is derived from the Building base class and implements
 *          functionality specific to residential structures such as houses and apartments
 */
class ResidentialBuilding : public Building
{
private:
	/** @brief The amount of sewage produced by the residential building */
	int sewageProduction;

	/** @brief The amount of waste produced by the residential building */
	int wasteProduction;

public:
	/** @brief Default constructor */
	ResidentialBuilding() = default;

	/**
	 * @brief Gets the cost consumption of the residential building
	 * @return The cost consumption value as an integer
	 */
	virtual int getCostConsumption() override;

	/**
	 * @brief Gets the revenue generated by the residential building
	 * @return The revenue value as an integer
	 */
	virtual int getRevenue() override;

	/**
	 * @brief Gets the sewage production of the residential building
	 * @return The sewage production value as an integer
	 */
	virtual int getSewageProduction() override;

	/**
	 * @brief Gets the waste production of the residential building
	 * @return The waste production value as an integer
	 */
	virtual int getWasteProduction() override;
};

#endif